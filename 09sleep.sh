#!/bin/bash  
  
# start time  
date +"%H:%M:%S"  
  
echo "wait for 9 seconds"  
  
# sleep for 9 seconds  
sleep 9s   
# you can also use "sleep 9" in place of "sleep 9s" because if there is no suffix, it is considered as "seconds".  
  
  
# end time  
date +"%H:%M:%S"  
  
echo "Task Completed"  
